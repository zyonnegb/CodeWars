function switchItUp(n) {
    if (n === 0) return 'Zero';
    if (n === 1) return 'One';
    if (n === 2) return 'Two';
    if (n === 3) return 'Three';
    if (n === 4) return 'Four';
    if (n === 5) return 'Five';
    if (n === 6) return 'Six';
    if (n === 7) return 'Seven';
    if (n === 8) return 'Eight';
    if (n === 9) return 'Nine';
  }
  switchItUp(4);    // Four
  switchItUp(8);    // Eight
  switchItUp(100);  // undefined